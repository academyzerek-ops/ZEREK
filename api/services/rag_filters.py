def banned_words_for(format_id: str) -> List[str]:
    """Финальный список banned для конкретного формата (объединение
    base + brand + специфичные по формату).
    """
    out = list(_BANNED_BASE) + list(_BANNED_BY_FORMAT["_BRAND_"])
    fmt = (format_id or "").upper()
    if fmt.endswith("_HOME") or fmt.endswith("_SOLO") or fmt in ("MANICURE_HOME", "MANICURE_SOLO"):
        out += list(_BANNED_BY_FORMAT["_HOME_OR_SOLO"])
    return out


def find_banned(text: str, banned: List[str]) -> List[str]:
    """Возвращает список найденных запрещённых substring'ов."""
    if not text:
        return []
    low = text.lower()
    hits = []
    for w in banned:
        if w.lower() in low:
            hits.append(w)
    return hits
