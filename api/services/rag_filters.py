def get_format_context(format_id: str) -> str:
    """Возвращает текст ограничения для system-промпта."""
    fmt = (format_id or "").upper()
    if fmt in FORMAT_CONTEXTS:
        return FORMAT_CONTEXTS[fmt]
    if fmt.endswith("_HOME"):
        return FORMAT_CONTEXTS["_HOME_"]
    if fmt.endswith("_SOLO"):
        return FORMAT_CONTEXTS["_SOLO_"]
    return ""


