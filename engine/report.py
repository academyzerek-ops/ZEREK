"""
ZEREK Quick Check — Форматирование отчёта.
Принимает dict из engine.run_quick_check() и возвращает текстовый отчёт.
"""

def fmt(n) -> str:
    """Форматирует число с пробелами-разделителями."""
    try:
        return f"{int(n):,}".replace(",", " ")
    except:
        return str(n)


def pct(n) -> str:
    try:
        return f"{float(n):.1f}%"
    except:
        return str(n)


def render_report(result: dict) -> str:
    inp  = result["input"]
    mkt  = result["market"]
    cap  = result["capex"]
    fin  = result["financials"]
    be   = result["breakeven"]
    pb   = result["payback"]
    cf   = result["cashflow"]
    mac  = result["macro"]
    risk = result["risks"]
    rec  = result["recommendations"]
    tss  = mkt["tam_sam_som"]
    comp = mkt["competitors"]

    lines = []
    def h1(t): lines.append(f"\n{'═'*60}\n  {t}\n{'═'*60}")
    def h2(t): lines.append(f"\n── {t} ──")
    def li(t): lines.append(f"  • {t}")
    def row(k, v): lines.append(f"  {k:<32} {v}")
    def sep(): lines.append("")

    # ── ШАПКА ──
    lines.append(f"""
╔══════════════════════════════════════════════════════════════╗
║           ZEREK — QUICK CHECK ОТЧЁТ                         ║
╚══════════════════════════════════════════════════════════════╝
  Город:    {inp['city_name']}
  Ниша:     {inp['niche_id']}
  Формат:   {inp['format_id']}
  Площадь:  {inp['area_m2']} м²
  Локация:  {inp['loc_type']}
  Капитал:  {fmt(inp['capital'])} ₸
  Уровень:  {inp['capex_level'].upper()}
""")

    # ── БЛОК 1: РЫНОК ──
    h1("1. РЫНОК И АУДИТОРИЯ")

    h2("Население города")
    row("Всё население:", fmt(mkt['population']) + " чел.")
    row("Трудоспособные 16-62:", fmt(mkt['working_age']) + " чел.")
    row("Мужчины 18-45:", fmt(mkt['men_1845']) + " чел.")
    row("Женщины 18-50:", fmt(mkt['women_1850']) + " чел.")
    row("Темп роста населения:", pct(mkt.get('growth_rate', 0)))
    sep()

    h2("TAM / SAM / SOM")
    row("TAM (вся ниша в городе):", fmt(tss['tam']) + " чел.")
    row("SAM (охват 1 точки ~2 км):", fmt(tss['sam']) + " чел.")
    row("SOM (реалистичная доля):", fmt(tss['som']) + " чел.")
    sep()

    h2("Конкурентная среда")
    row("Конкурентов в городе:", str(comp.get('кол_во', '?')))
    li(comp.get('сигнал', ''))
    if comp.get('лидеры'):
        li(f"Игроки рынка: {comp['лидеры']}")
    sep()

    # ── БЛОК 2: CAPEX ──
    h1("2. СТАРТОВЫЕ ВЛОЖЕНИЯ (CAPEX)")

    row("Оборудование и ремонт:", fmt(cap['оборудование_и_ремонт']) + " ₸")
    row("Депозит аренды (2 мес.):", fmt(cap['депозит_аренды']) + " ₸")
    row("ИТОГО нужно:", fmt(cap['итого']) + " ₸")
    row("Ваш капитал:", fmt(cap['капитал_пользователя']) + " ₸")
    sep()
    lines.append(f"  {cap['сигнал']}")
    sep()
    lines.append("  ⚠️  Частые статьи которые забывают:")
    li("Резервный фонд на 2-3 месяца OPEX")
    li("Разрешительные документы и лицензии")
    li("Маркетинг на старт")
    li("Реальная стоимость ремонта (+30-50% к смете)")
    sep()

    # ── БЛОК 3: ФИНАНСОВАЯ МОДЕЛЬ ──
    h1("3. ФИНАНСОВАЯ МОДЕЛЬ")

    h2("Базовые параметры (зрелый месяц)")
    row("Средний чек:", fmt(fin['чек']) + " ₸")
    row("Трафик в день:", str(fin['трафик_день']) + " чел./авт.")
    row("Выручка в мес. (база):", fmt(fin['базовая_выручка_мес']) + " ₸")
    row("Себестоимость (COGS):", pct(fin['cogs_pct']) + " от выручки")
    row("ФОТ с налогами:", fmt(fin['fot_мес']) + " ₸/мес")
    row("Аренда:", fmt(fin['аренда_мес']) + " ₸/мес")
    row("Операционная маржа:", pct(fin['маржа_опер_pct']))
    sep()

    h2("Итоги первого года")
    row("Выручка за 12 мес.:", fmt(fin['выручка_год1']) + " ₸")
    row("Прибыль за 12 мес.:", fmt(fin['прибыль_год1']) + " ₸")
    row("Средняя прибыль/мес (год 1):", fmt(fin['прибыль_среднемес_год1']) + " ₸")
    row("Прогноз прибыль/мес (год 2):", fmt(fin['прибыль_среднемес_год2_прогноз']) + " ₸")
    sep()

    # ── БЛОК 4: ТОЧКА БЕЗУБЫТОЧНОСТИ ──
    h1("4. ТОЧКА БЕЗУБЫТОЧНОСТИ")

    row("ТБ по выручке:", fmt(be['тб_выручка']) + " ₸/мес")
    row("ТБ в единицах (мес.):", fmt(be['тб_единиц_мес']) + " шт./мес")
    row("ТБ в единицах (день):", str(be['тб_единиц_день']) + " шт./день")
    row("Ваш трафик (день):", str(be['текущий_трафик']) + " шт./день")
    запас = be.get('запас_прочности_%', 0)
    if запас > 0:
        row("Запас прочности:", pct(запас) + " выше ТБ ✅")
    else:
        row("Запас прочности:", f"{pct(abs(запас))} НИЖЕ ТБ 🔴")
    sep()

    # ── БЛОК 5: ОКУПАЕМОСТЬ ──
    h1("5. ОКУПАЕМОСТЬ")

    row("CAPEX всего:", fmt(cap['итого']) + " ₸")
    lines.append(f"  {pb['статус']}")
    if pb.get('месяц'):
        row("Срок окупаемости:", str(pb['месяц']) + " мес.")
    sep()

    # ── БЛОК 6: CASH FLOW 12 МЕСЯЦЕВ ──
    h1("6. CASH FLOW — 12 МЕСЯЦЕВ")

    lines.append(f"  {'Мес':>4}  {'Кал':>4}  {'Выручка':>12}  {'OPEX':>12}  {'Прибыль':>12}  {'Нараст.':>14}")
    lines.append("  " + "─" * 62)
    for row_cf in cf:
        прибыль_str = fmt(row_cf['прибыль'])
        нараст_str  = fmt(row_cf['нарастающий'])
        знак_пр  = "+" if row_cf['прибыль'] >= 0 else ""
        знак_нар = "+" if row_cf['нарастающий'] >= 0 else ""
        lines.append(
            f"  {row_cf['месяц']:>4}  {row_cf['кал_месяц']:>4}  "
            f"{fmt(row_cf['выручка']):>12}  "
            f"{fmt(row_cf['opex']):>12}  "
            f"{знак_пр}{прибыль_str:>11}  "
            f"{знак_нар}{нараст_str:>13}"
        )
    sep()

    # ── БЛОК 7: МАКРО И ПРОГНОЗ ──
    h1("7. МАКРОЭКОНОМИКА И ПРОГНОЗ")

    row("Инфляция в регионе (фев 2026):", pct(mac['инфляция_регион_%']))
    row("Прогноз роста OPEX (год 2):", pct(mac['рост_opex_прогноз_%']) + "/год")
    sep()
    li("Аренда и зарплаты будут расти — закладывай в финмодель")
    li("Для аптеки и продуктового — курсовой риск на импорт")
    sep()

    # ── БЛОК 8: РИСКИ ──
    h1("8. РИСКИ")

    h2("Паттерны закрытий по этой нише")
    failure = risk.get('паттерн_закрытий', {})
    if failure:
        row("Медиана закрытия:", str(failure.get('Медиана месяца закрытия', '?')))
        row("Топ-1 причина:", str(failure.get('Топ-1 причина', '?')))
        row("Топ-2 причина:", str(failure.get('Топ-2 причина', '?')))
        row("Закрылись до 6 мес.:", str(failure.get('Доля закрытых до 6 мес. (%)', '?')))
        sep()
        фраза = failure.get('Фраза для отчёта Quick Check', '')
        if фраза:
            lines.append(f"  📌 {фраза}")
        sep()

    h2("Разрешения и лицензии")
    permits = risk.get('разрешения', [])
    for p in permits[:6]:  # показываем первые 6
        must = "‼️" if p.get("Обязательность") == "Обязательно" else "ℹ️"
        name = p.get("Документ / разрешение", "")
        cost = p.get("Стоимость (₸)", "")
        term = p.get("Срок получения", "")
        li(f"{must} {name} — {term}, {cost}")
    sep()

    # ── БЛОК 9: РЕКОМЕНДАЦИИ ──
    h1("9. РЕКОМЕНДАЦИИ")

    h2("Налоговый режим")
    tax = rec.get('налоговый_режим', {})
    row("Режим:", str(tax.get('Рекомендуемый режим', '?')))
    row("Регистрация:", str(tax.get('Регистрация', '?')))
    row("Ограничения:", str(tax.get('Ограничения', '—')))
    sep()

    h2("Поведение клиентов")
    cons = rec.get('поведение_клиентов', {})
    if cons:
        row("Средний чек рынка:", fmt(cons.get('Средний чек (₸)', 0)) + " ₸")
        row("Частота визита:", str(cons.get('Частота визита', '?')))
        row("Пиковые часы:", str(cons.get('Пиковые часы', '?')))
        row("Пиковые дни:", str(cons.get('Пиковые дни', '?')))
        row("Повторные клиенты:", str(cons.get('Доля повторных клиентов %', '?')))
        row("Канал поиска:", str(cons.get('Основной канал поиска', '?')))
    sep()

    # ── ПОДВАЛ ──
    lines.append("═" * 60)
    lines.append("  Следующий шаг: получи детальную финансовую модель в ZEREK")
    lines.append("  @zerek_app | zerek.cc")
    lines.append("═" * 60)
    lines.append("")

    return "\n".join(lines)
